numero = input("Digite um número: ") #variável que salva o número com a respota do usuário
if numero % 2 == 0: # Se o número que o usuário digitar for divisível por 2 e restar zero retorna a mensagem abaixo
    print("O número é par!")
else: # se não retorna a mensagem abaixo
    print("O número é ímpar!")    

